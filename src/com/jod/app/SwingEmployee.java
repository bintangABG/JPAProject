    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        EntityManager em = emf.createEntityManager();
        Employee e = new Employee();
        e.setName(namaTextField.getText());
        e.setPosition(jabatanTextField.getText());
        double salary = Double.parseDouble(salaryTextField.getText());
        e.setSalary(salary);
        e.setDateOfBirth(tanggalLahirDateChooser.getDate());

        em.getTransaction().begin();

        em.persist(e);

        em.getTransaction().commit();
        em.close();

        DefaultTableModel tableModel = (DefaultTableModel) tableEmployee.getModel();
        tableModel.setRowCount(0);
        showEmployee();

    }//GEN-LAST:event_saveButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:

        tableEmployee.clearSelection();
        EntityManager em = emf.createEntityManager();
        EntityTransaction transaction = em.getTransaction();
        transaction.begin();

        Employee deletePerson = em.find(Employee.class, emplo.getId());
        em.remove(deletePerson);
        em.getTransaction().commit();
        em.close();

        DefaultTableModel tableModel = (DefaultTableModel) tableEmployee.getModel();
        tableModel.setRowCount(0);
        showEmployee();
        
        JOptionPane.showMessageDialog(rootPane, "Data Berhasil Dihapus");
        namaTextField.setText(null);
        jabatanTextField.setText(null);
        salaryTextField.setText(null);
        tanggalLahirDateChooser.setDate(null);
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        tableEmployee.clearSelection();
        EntityManager em = emf.createEntityManager();
        EntityTransaction transaction = em.getTransaction();
        transaction.begin();

        Employee updateomp = emplo;
        updateomp.setName(namaTextField.getText());
        updateomp.setPosition(jabatanTextField.getText());
//        updateomp.setSalary(()salaryTextField.getText());
        updateomp.setDateOfBirth(tanggalLahirDateChooser.getDate());
        em.merge(updateomp);
        em.getTransaction().commit();
        em.close();

        DefaultTableModel tableModel = (DefaultTableModel) tableEmployee.getModel();
        tableModel.setRowCount(0);
        showEmployee();
        
        JOptionPane.showMessageDialog(null, "Data Berhasil Dirubah");
        namaTextField.setText(null);
        jabatanTextField.setText(null);
        salaryTextField.setText(null);
        tanggalLahirDateChooser.setDate(null);
    }//GEN-LAST:event_updateButtonActionPerformed

