    public void showEmployee() {
        DateFormat df = new SimpleDateFormat("dd MMM yyyy");
        List<Employee> employee = getEmployee();
        DefaultTableModel tabelModel = (DefaultTableModel) tableEmployee.getModel();
        Object[] row = new Object[5];
        for (Employee emp : employee) {
            row[0] = emp.getId();
            row[1] = emp.getName();
            row[2] = emp.getPosition();
            row[3] = emp.getSalary();
            row[4] = df.format(emp.getDateOfBirth());
            tabelModel.addRow(row);
        }
        ListSelectionModel rowSelMod = tableEmployee.getSelectionModel();
        rowSelMod.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting()) {

                    int i = tableEmployee.getSelectedRow();
//                    System.out.println("nilai i " + i);
                    if (i != -1) {
                        int idemp =  (int) tableEmployee.getValueAt(i, 0);
                        EntityManager em = emf.createEntityManager();
                        Employee person = em.find(Employee.class, idemp);
                        emplo = person;
                        fillFormWith(person);
                    }
                }

            }
        });
    }

    private void fillFormWith(Employee emp) {
        namaTextField.setText(emp.getName());

        DecimalFormat df = (DecimalFormat) NumberFormat.getInstance();
        df.applyPattern("###,###.##");
        String stringSalary = df.format(emp.getSalary());
        salaryTextField.setText(stringSalary);
        jabatanTextField.setText(emp.getPosition());
        tanggalLahirDateChooser.setDate(emp.getDateOfBirth());
    }

    private List getEmployee() {
        EntityManager em = emf.createEntityManager();
        List<Employee> employee = em.createQuery("Select e from Employee e").getResultList();
        em.close();
        return employee;
    }

